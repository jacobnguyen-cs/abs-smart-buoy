package restclient;

public class BroadcastInfo 
{
    public String ID  = null;
    public String Description = null;
    
    public BroadcastInfo()
    {
        
    }
    
    public BroadcastInfo(String ID, String Description)
    {
        super();
        
        this.ID = ID;
        this.Description = Description;
    }
}
